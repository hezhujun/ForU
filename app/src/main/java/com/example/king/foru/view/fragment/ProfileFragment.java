    LinearLayout profileBg;
    @BindView(R.id.profile_listview1)
    ListView listView1;
    @BindView(R.id.profile_listview2)
    ListView ListView2;
    @BindView(R.id.profile_quit_btn)
    Button quitBtn;
    @BindView(R.id.login_iv)
    ImageView login_iv;
    @BindView(R.id.profile_tv_username)
    TextView username;

    List<ProfileItem> datas1;
    List<ProfileItem> datas2;



